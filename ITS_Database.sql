CREATE TABLE DEPARTMENT (
DEPT_ID INTEGER,
DEPT_NAME VARCHAR(35) NOT NULL,
BUDGET INTEGER,
PRIMARY KEY (DEPT_ID));

CREATE TABLE TECHNICIAN (
TECH_ID INTEGER,
LNAME VARCHAR(35) NOT NULL,
FNAME VARCHAR(35) NOT NULL,
EMAIL VARCHAR(35) NOT NULL,
OFFICE_NUM INTEGER,
PHONE_NUM VARCHAR(35),
POSITION VARCHAR(35),
DEPT_ID INTEGER,
CONSTRAINT TECHNICIAN_DEPT_ID_FK FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT (DEPT_ID),
PRIMARY KEY (TECH_ID));

CREATE TABLE STUDENT (
STU_ID INTEGER,
LNAME VARCHAR(35) NOT NULL,
FNAME VARCHAR(35) NOT NULL,
EMAIL VARCHAR(35),
POSITION VARCHAR(35),
TECH_ID INTEGER,
CONSTRAINT STUDENT_TECH_ID_FK FOREIGN KEY (TECH_ID) REFERENCES TECHNICIAN (TECH_ID),
PRIMARY KEY (STU_ID));

CREATE TABLE SERVICE (
SERVICE_ID INTEGER,
SERVICE_DESC VARCHAR(35),
PRIMARY KEY (SERVICE_ID));

CREATE TABLE CLIENTS (
CLIENT_ID INTEGER,
LNAME VARCHAR(35) NOT NULL,
FNAME VARCHAR(35) NOT NULL,
EMAIL VARCHAR(35),
STU_ID INTEGER,
SERVICE_ID INTEGER,
CONSTRAINT CLIENTS_STU_ID_FK FOREIGN KEY (STU_ID) REFERENCES STUDENT (STU_ID),
CONSTRAINT CLIENTS_SERVICE_ID_FK FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (SERVICE_ID),
PRIMARY KEY (CLIENT_ID));

CREATE TABLE LAB_ROOM (
ROOM_NUM VARCHAR(5),
COMP_QUANT INTEGER NOT NULL,
PRINTER BOOL NOT NULL,
LAB_TYPE VARCHAR(35),
TECH_ID INTEGER,
CONSTRAINT LAB_ROOM_TECH_ID_FK FOREIGN KEY (TECH_ID) REFERENCES TECHNICIAN (TECH_ID),
PRIMARY KEY (ROOM_NUM));

CREATE TABLE CLASSES (
CLASS_ID VARCHAR(6),
CLASS_DESC VARCHAR(35) NOT NULL,
CLASS_CREDIT INTEGER,
ENROLLED INTEGER,
PRIMARY KEY (CLASS_ID)); 

CREATE TABLE CLASS_SCHEDULE (
ROOM_NUM VARCHAR(5),
CLASS_ID VARCHAR(6),
START_TIME TIME,
END_TIME TIME,
CLASS_DAYS VARCHAR(3),
CONSTRAINT CLASS_SCHEDULE_ROOM_NUM_FK FOREIGN KEY (ROOM_NUM) REFERENCES LAB_ROOM (ROOM_NUM),
CONSTRAINT CLASS_SCHEDULE_CLASS_ID_FK FOREIGN KEY (CLASS_ID) REFERENCES CLASSES (CLASS_ID),
PRIMARY KEY (ROOM_NUM, CLASS_ID));

CREATE TABLE SOFTWARE (
SOFTWARE_ID INTEGER,
SOFTWARE_NAME VARCHAR(35) NOT NULL,
VERSION VARCHAR(35) NOT NULL,
PRIMARY KEY (SOFTWARE_ID));

CREATE TABLE EQUIPMENT (
EQUIP_ID INTEGER,
EQUIP_DESC VARCHAR(35) NOT NULL,
QUANTITY INTEGER,
PRIMARY KEY (EQUIP_ID));

CREATE TABLE RENTAL (
EQUIP_ID INTEGER,
CLIENT_ID INTEGER,
RENTAL_DATE DATE,
EVENT_TIME TIME,
RETURN_TIME TIME,
EVENT_LOCATION VARCHAR(35),
CONSTRAINT RENTAL_EQUIP_ID_FK FOREIGN KEY (EQUIP_ID) REFERENCES EQUIPMENT (EQUIP_ID),
CONSTRAINT RENTAL_CLIENT_ID_FK FOREIGN KEY (CLIENT_ID) REFERENCES CLIENTS (CLIENT_ID),
PRIMARY KEY (EQUIP_ID, CLIENT_ID));

CREATE TABLE INSTALLS (
ROOM_NUM VARCHAR(5),
SOFTWARE_ID INTEGER,
CONSTRAINT INSTALLS_ROOM_NUM_FK FOREIGN KEY (ROOM_NUM) REFERENCES LAB_ROOM (ROOM_NUM),
CONSTRAINT INSTALLS_SOFTWARE_ID_FK FOREIGN KEY (SOFTWARE_ID) REFERENCES SOFTWARE (SOFTWARE_ID),
PRIMARY KEY (ROOM_NUM, SOFTWARE_ID));

